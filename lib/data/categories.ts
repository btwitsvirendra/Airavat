/**
 * Comprehensive Category List for Airavat B2B E-commerce Platform
 * Based on AladdinNow Categories
 */

export interface Category {
  id: string;
  name: string;
  slug: string;
  icon: string;
  description: string;
  subcategories?: Category[];
}

// Helper function to generate slug from name
function generateSlug(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

// Helper function to create category
function createCategory(
  name: string,
  icon: string,
  description: string,
  subcategoryNames?: string[] | Category[]
): Category {
  const slug = generateSlug(name);
  return {
    id: slug,
    name,
    slug,
    icon,
    description,
    subcategories: subcategoryNames?.map((sub) => {
      if (typeof sub === 'string') {
        return {
          id: generateSlug(`${slug}-${sub}`),
          name: sub,
          slug: generateSlug(`${slug}-${sub}`),
          icon: 'ðŸ“¦',
          description: '',
        };
      }
      return sub;
    }),
  };
}

// Helper function to create subcategory with micro-categories
function createSubcategory(
  parentSlug: string,
  name: string,
  microCategories?: string[]
): Category {
  const slug = generateSlug(`${parentSlug}-${name}`);
  return {
    id: slug,
    name,
    slug,
    icon: 'ðŸ“¦',
    description: '',
    subcategories: microCategories?.map((micro) => ({
      id: generateSlug(`${slug}-${micro}`),
      name: micro,
      slug: generateSlug(`${slug}-${micro}`),
      icon: 'ðŸ“¦',
      description: '',
    })),
  };
}

