// Helper function to get category by slug
export function getCategoryBySlug(slug: string): Category | undefined {
  const findInCategories = (cats: Category[]): Category | undefined => {
    for (const cat of cats) {
      if (cat.slug === slug) return cat;
      if (cat.subcategories) {
        const found = findInCategories(cat.subcategories);
        if (found) return found;
      }
    }
    return undefined;
  };
  return findInCategories(categories);
}

// Helper function to get all categories flattened
export function getAllCategories(): Category[] {
  const all: Category[] = [];
  const flatten = (cats: Category[]) => {
    cats.forEach((cat) => {
      all.push(cat);
      if (cat.subcategories) {
        flatten(cat.subcategories);
      }
    });
  };
  flatten(categories);
  return all;
}
